# The canonical Ubuntu+k3s template VMID. The cluster OpenTofu
# root reads the live value from build/image-id.txt (the SS1->SS2
# contract), so any future VMID bump just requires editing this
# constant + re-running the build. The default 900 matches the
# recipe in the operator skill and avoids a stuck LV on VMID 950
# (which holds an open handle from an earlier failed run that
# even `lvremove -f` could not release).
TEMPLATE_VMID = 900
IMAGE_ID_FILE = "image-id.txt"
DISK_STORAGE = "data1"
BRIDGE = "vnet0"
MEMORY_MB = 4096
CORES = 2
DISK_SIZE_GB = 32
AGENT_PING_TIMEOUT_S = 240
AGENT_PING_POLL_S = 5
AGENT_IP_POLL_TIMEOUT_S = 60
SHUTDOWN_TIMEOUT_S = 90

# Canonical Ubuntu 24.04 LTS (Noble) cloud image. The
# `noble/current/` URL always points to the latest daily; we pin
# via SHA256SUMS at fetch time.
UBUNTU_NOBLE_BASE_URL = "https://cloud-images.ubuntu.com/noble/current"
UBUNTU_IMAGE_NAME = "noble-server-cloudimg-amd64.img"
UBUNTU_SHA256_NAME = "SHA256SUMS"
UBUNTU_NOBLE_VERSION = "noble-24.04"

# k3s install URL (no version = track channel). The cluster
# OpenTofu module passes this to each clone's per-VM cloud-init
# runcmd when the cluster is applied; the template itself does
# NOT install k3s (k3s is per-cluster, not per-image).
K3S_INSTALL_URL = "https://get.k3s.io"

LOG_DIR_DEFAULT = Path("logs")

# -----------------------------------------------------------------------------
# Helpers
# -----------------------------------------------------------------------------


def _utc_stamp() -> str:
    return datetime.now(timezone.utc).strftime("%Y%m%dT%H%M%SZ")


def _one_line(s: str, *, maxlen: int = 240) -> str:
    s = s.replace("\n", " ").replace("\r", " ")
    return s if len(s) <= maxlen else s[: maxlen - 1] + "..."


def _first_pubkey_line(path: Path) -> str:
    """Read an SSH public key file and return only the first non-empty line.

    Bitwarden-exported pubkey files can contain multiple keys (one
    per ``ssh-add -L`` line). Cloud-init's ``ssh_authorized_keys:``
    schema requires a single key per list item — feeding a multi-line
    blob breaks the YAML parse with "line 11 column 1".
    """
    text = path.read_text().strip()
    for line in text.splitlines():
        line = line.strip()
        if line and not line.startswith("#"):
            return line
    raise ValueError(f"no non-comment pubkey line in {path}")


