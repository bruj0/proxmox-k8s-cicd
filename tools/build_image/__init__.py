"""Build the Ubuntu 24.04 LTS + k3s Proxmox template.

This is the SS1 golden-image builder for the proxmox-k8s-cicd
pipeline. The output is a Proxmox TEMPLATE (VMID ``TEMPLATE_VMID``,
default 900) containing:

  * Ubuntu 24.04 LTS Noble Server cloud image (qcow2 from
    cloud-images.ubuntu.com) imported as ``scsi0`` and converted to
    a ``base-900-disk-1`` thin LV.
  * ``qemu-guest-agent``, ``openssh-server`` and ``cloud-init``
    packages pre-installed and ``qemu-guest-agent``/``ssh`` units
    enabled. The bake is done with ``virt-customize`` (libguestfs)
    on the PVE host, BEFORE the VM is created, so the agent is
    guaranteed present on first boot (no race with cloud-init).
  * Proxmox's native cloud-init drive attached as ``ide2`` with
    ``--ciuser ubuntu`` and the operator's SSH pubkey. The
    cloud-init drive is regenerated by PVE on every ``qm start``
    from the stored ``--ciuser``/``--sshkeys``/``--ipconfig0``
    config, so clones only need to override these per-VM with
    their own ``qm set`` (which the cluster OpenTofu module does
    in the per-VM ``proxmox_cloned_vm`` block).
  * OVMF (UEFI) firmware, q35 machine, agent enabled, serial
    console on socket, VGA-on-serial — the canonical Proxmox
    recipe verified to boot cleanly with Ubuntu 24.04 cloud images.

The previous implementation tried to (a) apt-install qemu-guest-agent
inside a post-create customize SSH step, which failed silently so the
template booted without the agent and all 4 cluster clones were stuck
in initramfs; and (b) mount the imported root disk via losetup to
inject a serial-console GRUB cmdline, which corrupted the LVM
backing. This rewrite follows the canonical Proxmox+Ubuntu recipe
documented in the operator skill:

    https://github.com/proxmox/virtual-environment docs
        "Creating a Ubuntu 24.04 Cloud-Init VM Template on Proxmox VE"

CLI
---
  python -m tools.build_image \\
    --pve-endpoint https://kvm.bruj0.net:8006/api2/json \\
    --pve-node BigBertha \\
    --pve-token-id 'k3s-terraform@pam!tf' \\
    --pve-token-secret '<uuid>' \\
    --pve-ssh-host kvm.bruj0.net --pve-ssh-port 6022 \\
    --ssh-pubkey-path ~/.ssh/kvm.bruj0.net.pub \\
    --ubuntu-image-version noble \\
    --k3s-channel stable \\
    --log-dir ./logs

Output:
  * Proxmox template at VMID ``TEMPLATE_VMID``
  * ``build/image-id.txt`` containing the template VMID (the SS2
    contract file read by the cluster OpenTofu roots)
"""

